console.log('JavaScript Assignment Operators');

let a = 9;
let b = 4;

a += 8;
b += 3;
console.log(a, b); // 17, 7

a -= 7;
b -= 2;
console.log(a, b); // 10, 5

a *= 2;
b *= 3;
console.log(a, b); // 20, 15

a /= 8;
b /= 2;
console.log(a, b); // 2.5, 7.5

a **= 2;
b **= 2;
console.log(a, b); // 6.25, 56.25

a %= 3;
b %= 20;
console.log(a, b); // 0.25, 16.25

