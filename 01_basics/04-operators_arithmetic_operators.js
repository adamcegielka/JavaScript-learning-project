console.log('JavaScript Arithmetic Operators');

let a = 3;
let b = 1;

a = 12;
b = 5;

// Addition
const result1 = a + b;
console.log(result1); // 17

// Subtraction
const result2 = a - b;
console.log(result2); // 7

// Multiplication
const result3 = a * b;
console.log(result3); // 60

// Exponentiation
const result4 = a ** b; // 248832
console.log(result4);

// Division
const result5 = a / b; // 2.4
console.log(result5);

// Modulus
const result6 = a % b; // 2
console.log(result6);

